function normalizeGlossaryText(text: string): string {
  return (text || "")
    .toLowerCase()
    .replace(/[’']/g, "'")
    .replace(/[–—-]/g, " ")
    .replace(/[^\p{L}\p{N}\s']/gu, " ")
    .replace(/\s+/g, " ")
    .trim();
}


// Helper function to format the glossary for the prompt based on the requested language and source text
export function getGlossaryPrompt(langKey: string, sourceRows: string[][]): string {
  const glossary = TRANSLATION_GLOSSARY[langKey];
  
  // If there is no glossary for this language, return an empty string
  if (!glossary) {
    return ""; 
  }


  

  // Flatten all source rows into a single lowercase string for easy searching
const sourceText = normalizeGlossaryText(sourceRows.flat().join(" "));

  let promptText = "=== MANDATORY TRANSLATION GLOSSARY ===\n";
  promptText += "When you see these terms in the source, you MUST translate them exactly as shown below:\n";
  
  let matchedTerms = 0;

  // Only add terms that actually appear in the source text
  for (const [englishTerm, translatedTerm] of Object.entries(glossary)) {
   const normalizedEnglishTerm = normalizeGlossaryText(englishTerm);

if (sourceText.includes(normalizedEnglishTerm)) {
      promptText += `- "${englishTerm}" -> "${translatedTerm}"\n`;
      matchedTerms++;
    }
  }

  

  
  // If no glossary terms were found in this specific sheet, return an empty string
  if (matchedTerms === 0) {
    return "";
  }
  
  return promptText;
}